package com.gmail.programaker.joguin.character;

public class Character {
    private long id;
    private String name;
    private Gender gender;
    private int age;
    private float height;

    public long getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public Gender getGender() {
        return gender;
    }

    public int getAge() {
        return age;
    }

    public float getHeight() {
        return height;
    }
}
